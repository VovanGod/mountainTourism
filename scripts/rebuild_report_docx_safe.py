def style_for_text(text: str, index: int) -> dict:
    if index <= 18:
        return {"align": "center", "bold": text in {"КУРСОВАЯ РАБОТА"} or text.isupper(), "first_line": False}
    if text in {"РЕФЕРАТ", "СОДЕРЖАНИЕ", "ВВЕДЕНИЕ", "ЗАКЛЮЧЕНИЕ", "СПИСОК ИСПОЛЬЗОВАННЫХ ИСТОЧНИКОВ"}:
        return {"align": "center", "bold": True, "first_line": False, "before": 120, "after": 120}
    if re.match(r"^[123]\s", text):
        return {"align": "center", "bold": True, "first_line": False, "before": 120, "after": 120}
    if re.match(r"^\d+\.\d+\s", text):
        return {"align": "both", "bold": True, "first_line": False, "before": 120, "after": 60}
    if text.startswith("Рисунок ") or text.startswith("Таблица "):
        return {"align": "center", "italic": False, "first_line": False, "before": 60, "after": 60}
    if re.match(r"^\d+\. ", text):
        return {"align": "both", "first_line": False}
    return {"align": "both", "first_line": True}


