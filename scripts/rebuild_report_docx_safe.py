NS = {
    "w": "http://schemas.openxmlformats.org/wordprocessingml/2006/main",
    "r": "http://schemas.openxmlformats.org/officeDocument/2006/relationships",
}


def qn(name: str) -> str:
    prefix, tag = name.split(":", 1)
    return f"{{{NS[prefix]}}}{tag}"


def xml_text(value: str) -> str:
    return html.escape(value, quote=False)


def png_size(path: Path) -> tuple[int, int]:
    data = path.read_bytes()
    if data[:8] != b"\x89PNG\r\n\x1a\n":
        raise ValueError(f"{path} is not a PNG")
    return struct.unpack(">II", data[16:24])


def run_xml(text: str, bold: bool = False, italic: bool = False) -> str:
    rpr = ""
    if bold or italic:
        rpr_parts = []
        if bold:
            rpr_parts.append("<w:b/>")
        if italic:
            rpr_parts.append("<w:i/>")
        rpr = f"<w:rPr>{''.join(rpr_parts)}</w:rPr>"

    escaped = xml_text(text)
    space = ' xml:space="preserve"' if text[:1].isspace() or text[-1:].isspace() else ""
    return f"<w:r>{rpr}<w:t{space}>{escaped}</w:t></w:r>"


def paragraph_xml(
    text: str = "",
    *,
    align: str = "both",
    bold: bool = False,
    italic: bool = False,
    first_line: bool = True,
    before: int = 0,
    after: int = 0,
    page_break: bool = False,
) -> str:
    indent = '<w:ind w:firstLine="708"/>' if first_line else ""
    page = '<w:r><w:br w:type="page"/></w:r>' if page_break else ""
    return (
        "<w:p>"
        "<w:pPr>"
        f'<w:spacing w:before="{before}" w:after="{after}" w:line="360" w:lineRule="auto"/>'
        f"{indent}"
        f'<w:jc w:val="{align}"/>'
        "</w:pPr>"
        f"{page}{run_xml(text, bold=bold, italic=italic) if text else ''}"
        "</w:p>"
    )


def table_xml(rows: list[list[str]]) -> str:
    border = (
        '<w:tblBorders>'
        '<w:top w:val="single" w:sz="4" w:space="0" w:color="000000"/>'
        '<w:left w:val="single" w:sz="4" w:space="0" w:color="000000"/>'
        '<w:bottom w:val="single" w:sz="4" w:space="0" w:color="000000"/>'
        '<w:right w:val="single" w:sz="4" w:space="0" w:color="000000"/>'
        '<w:insideH w:val="single" w:sz="4" w:space="0" w:color="000000"/>'
        '<w:insideV w:val="single" w:sz="4" w:space="0" w:color="000000"/>'
        "</w:tblBorders>"
    )
    xml = ['<w:tbl><w:tblPr><w:tblW w:w="0" w:type="auto"/>', border, "</w:tblPr>"]
    for row_index, row in enumerate(rows):
        xml.append("<w:tr>")
        for cell in row:
            xml.append(
                "<w:tc>"
                '<w:tcPr><w:tcW w:w="3000" w:type="dxa"/></w:tcPr>'
                + paragraph_xml(cell, bold=row_index == 0, first_line=False)
                + "</w:tc>"
            )
        xml.append("</w:tr>")
    xml.append("</w:tbl>")
    return "".join(xml)


