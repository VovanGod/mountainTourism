def expand_sequence(sequence: list[tuple[str, object]]) -> list[tuple[str, object]]:
    expanded: list[tuple[str, object]] = []
    ui_heading_count = 0

    for kind, value in sequence:
        if kind == "paragraph":
            text = humanize_text(str(value))
            if text.startswith("Курсовая работа содержит не менее 30 с."):
                text = "Курсовая работа содержит не менее 30 с., 7 рис., 3 табл., 15 источников."
            expanded.append((kind, text))

            if text.startswith("3 ") and "интерфейса" in text.lower():
                ui_heading_count += 1
                if ui_heading_count == 2:
                    expanded.extend(expanded_ui_sequence())
        else:
            expanded.append((kind, value))

    return expanded


def extract_sequence() -> list[tuple[str, object]]:
    with zipfile.ZipFile(SOURCE_DOCX) as archive:
        root = ET.fromstring(archive.read("word/document.xml"))

    body = root.find("w:body", NS)
    result: list[tuple[str, object]] = []
    for child in list(body):
        if child.tag == qn("w:p"):
            has_page_break = child.find(".//w:br", NS) is not None
            text = "".join((t.text or "") for t in child.findall(".//w:t", NS)).strip()
            has_drawing = child.find(".//w:drawing", NS) is not None
            if has_page_break:
                result.append(("page_break", None))
            if text:
                result.append(("paragraph", text))
            elif has_drawing:
                result.append(("drawing", None))
        elif child.tag == qn("w:tbl"):
            rows = []
            for tr in child.findall("w:tr", NS):
                row = []
                for tc in tr.findall("w:tc", NS):
                    cell = " ".join(
                        "".join((t.text or "") for t in p.findall(".//w:t", NS)).strip()
                        for p in tc.findall("w:p", NS)
                    ).strip()
                    row.append(cell)
                if row:
                    rows.append(row)
            if rows:
                result.append(("table", rows))
    return result


def build_document_xml(sequence: list[tuple[str, object]]) -> str:
    parts = []
    image_index = 0
    paragraph_index = 0
    for kind, value in sequence:
        if kind == "page_break":
            parts.append(paragraph_xml(page_break=True, first_line=False))
        elif kind == "paragraph":
            text = str(value)
            match = re.match(r"Рисунок 3\.(\d+)", text)
            if match:
                image_index = int(match.group(1))
                image_path = ASSETS_DIR / f"{image_index:02d}_" / "missing.png"
                image_lookup = {
                    1: "01_home_weather_emergency_quiz.png",
                    2: "02_favorites_page.png",
                    3: "03_route_detail_gpx_reviews.png",
                    4: "04_offline_page.png",
                    5: "05_quiz_page.png",
                    6: "06_animal_detail.png",
                    7: "07_safety_page.png",
                }
                image_path = ASSETS_DIR / image_lookup[image_index]
                parts.append(image_xml(image_path, f"rId{20 + image_index}", image_index))
