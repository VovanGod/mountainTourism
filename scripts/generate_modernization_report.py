NS = {
    "w": "http://schemas.openxmlformats.org/wordprocessingml/2006/main",
    "r": "http://schemas.openxmlformats.org/officeDocument/2006/relationships",
    "wp": "http://schemas.openxmlformats.org/drawingml/2006/wordprocessingDrawing",
    "a": "http://schemas.openxmlformats.org/drawingml/2006/main",
    "pic": "http://schemas.openxmlformats.org/drawingml/2006/picture",
    "rel": "http://schemas.openxmlformats.org/package/2006/relationships",
}

for prefix, uri in NS.items():
    if prefix != "rel":
        ET.register_namespace(prefix, uri)
ET.register_namespace("xml", "http://www.w3.org/XML/1998/namespace")


def qn(name: str) -> str:
    prefix, tag = name.split(":", 1)
    return f"{{{NS[prefix]}}}{tag}"


def clone(element):
    return copy.deepcopy(element) if element is not None else None


def clear_element(element):
    for child in list(element):
        element.remove(child)


def text_of(paragraph) -> str:
    return "".join((t.text or "") for t in paragraph.findall(".//w:t", NS))


def replace_paragraph_text(paragraph, text: str) -> None:
    texts = paragraph.findall(".//w:t", NS)
    if not texts:
        run = ET.SubElement(paragraph, qn("w:r"))
        text_node = ET.SubElement(run, qn("w:t"))
        text_node.text = text
        return

    texts[0].text = text
    if text[:1].isspace() or text[-1:].isspace():
        texts[0].set("{http://www.w3.org/XML/1998/namespace}space", "preserve")
    for extra in texts[1:]:
        extra.text = ""


def remove_num_pr(ppr):
    if ppr is None:
        return
    num_pr = ppr.find("w:numPr", NS)
    if num_pr is not None:
        ppr.remove(num_pr)


def make_ppr(base_ppr=None, jc: str | None = None, remove_numbering: bool = False):
    ppr = clone(base_ppr)
    if ppr is None:
        ppr = ET.Element(qn("w:pPr"))
    if remove_numbering:
        remove_num_pr(ppr)
    if jc:
        jc_el = ppr.find("w:jc", NS)
        if jc_el is None:
            jc_el = ET.SubElement(ppr, qn("w:jc"))
        jc_el.set(qn("w:val"), jc)
    return ppr


def make_run_with_text(parent, text: str, bold: bool = False, italic: bool = False) -> None:
    run = ET.SubElement(parent, qn("w:r"))
    if bold or italic:
        rpr = ET.SubElement(run, qn("w:rPr"))
        if bold:
            ET.SubElement(rpr, qn("w:b"))
        if italic:
            ET.SubElement(rpr, qn("w:i"))

    parts = re.split(r"(\n|\t)", text)
    first_text = True
    for part in parts:
        if part == "\n":
            ET.SubElement(run, qn("w:br"))
        elif part == "\t":
            ET.SubElement(run, qn("w:tab"))
        elif part:
            text_node = ET.SubElement(run, qn("w:t"))
            if first_text and (part[:1].isspace() or part[-1:].isspace()):
                text_node.set("{http://www.w3.org/XML/1998/namespace}space", "preserve")
            text_node.text = part
            first_text = False


def paragraph(text: str = "", ppr=None, jc: str | None = None, bold: bool = False, italic: bool = False):
    p = ET.Element(qn("w:p"))
    use_ppr = make_ppr(ppr, jc=jc)
    if use_ppr is not None:
        p.append(use_ppr)
    if text:
        make_run_with_text(p, text, bold=bold, italic=italic)
    return p


def page_break():
    p = ET.Element(qn("w:p"))
    r = ET.SubElement(p, qn("w:r"))
    br = ET.SubElement(r, qn("w:br"))
    br.set(qn("w:type"), "page")
    return p


def png_size(path: Path) -> tuple[int, int]:
    data = path.read_bytes()
    if data[:8] != b"\x89PNG\r\n\x1a\n":
        raise ValueError(f"{path} is not a PNG image")
    return struct.unpack(">II", data[16:24])


