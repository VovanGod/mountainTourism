def table(headers: list[str], rows: list[list[str]], base_ppr):
    tbl = ET.Element(qn("w:tbl"))
    tbl_pr = ET.SubElement(tbl, qn("w:tblPr"))
    tbl_w = ET.SubElement(tbl_pr, qn("w:tblW"))
    tbl_w.set(qn("w:w"), "0")
    tbl_w.set(qn("w:type"), "auto")
    borders = ET.SubElement(tbl_pr, qn("w:tblBorders"))
    for side in ("top", "left", "bottom", "right", "insideH", "insideV"):
        border = ET.SubElement(borders, qn(f"w:{side}"))
        border.set(qn("w:val"), "single")
        border.set(qn("w:sz"), "4")
        border.set(qn("w:space"), "0")
        border.set(qn("w:color"), "000000")

    def add_row(values, header=False):
        tr = ET.SubElement(tbl, qn("w:tr"))
        for value in values:
            tc = ET.SubElement(tr, qn("w:tc"))
            tc_pr = ET.SubElement(tc, qn("w:tcPr"))
            tc_w = ET.SubElement(tc_pr, qn("w:tcW"))
            tc_w.set(qn("w:w"), "3000")
            tc_w.set(qn("w:type"), "dxa")
            tc.append(paragraph(value, base_ppr, bold=header))

    add_row(headers, header=True)
    for row in rows:
        add_row(row)
    return tbl


