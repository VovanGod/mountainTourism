def replace_images_in_package(zin, zout):
    replacements = {
        "word/media/image1.png": ASSETS_DIR / "diagram_01_architecture.png",
        "word/media/image2.png": ASSETS_DIR / "diagram_02_user_flow.png",
        "word/media/image3.png": ASSETS_DIR / "diagram_03_data_model.png",
        "word/media/image4.png": ASSETS_DIR / "diagram_04_api_offline.png",
    }
    for item in zin.infolist():
        if item.filename in {"word/document.xml"}:
            continue
        if item.filename in replacements:
            zout.write(replacements[item.filename], item.filename)
        else:
            zout.writestr(item, zin.read(item.filename))


def build_report() -> Path:
    if not TEMPLATE_DOCX.exists():
        raise FileNotFoundError(TEMPLATE_DOCX)

    with zipfile.ZipFile(TEMPLATE_DOCX, "r") as archive:
        document_xml = archive.read("word/document.xml")
    root = ET.fromstring(document_xml)
    body = root.find("w:body", NS)
    children = list(body)
    section_properties = clone(body.find("w:sectPr", NS))

    update_title_page(body)
    fill_passport_tables(body)

    # Keep title page, passport and "Отчет о реализации проекта"; rebuild the report body.
    prefix = [clone(child) for child in list(body)[:85]]
    report_body = build_main_sections(children)

    for child in list(body):
        body.remove(child)
    for child in prefix + report_body:
        body.append(child)
    if section_properties is not None:
        body.append(section_properties)

    root.set(qn("mc:Ignorable"), "w14 wp14")
    document_bytes = ET.tostring(root, encoding="utf-8", xml_declaration=True)

    temp = OUTPUT_DOCX.with_suffix(".tmp.docx")
    if temp.exists():
        temp.unlink()
    with zipfile.ZipFile(TEMPLATE_DOCX, "r") as zin, zipfile.ZipFile(temp, "w", zipfile.ZIP_DEFLATED) as zout:
        replace_images_in_package(zin, zout)
        zout.writestr("word/document.xml", document_bytes)

    shutil.move(str(temp), str(OUTPUT_DOCX))
    return OUTPUT_DOCX


if __name__ == "__main__":
    print(build_report())
