for prefix, uri in NS.items():
    if prefix != "rel":
        ET.register_namespace(prefix, uri)


def qn(name: str) -> str:
    prefix, tag = name.split(":", 1)
    return f"{{{NS[prefix]}}}{tag}"


def clone(element):
    return copy.deepcopy(element)


def paragraph_text(paragraph) -> str:
    return "".join((node.text or "") for node in paragraph.findall(".//w:t", NS)).strip()


def replace_paragraph_text(paragraph, text: str) -> None:
    texts = paragraph.findall(".//w:t", NS)
    if not texts:
        run = ET.SubElement(paragraph, qn("w:r"))
        text_node = ET.SubElement(run, qn("w:t"))
        text_node.text = text
        return

    texts[0].text = text
    if text[:1].isspace() or text[-1:].isspace():
        texts[0].set("{http://www.w3.org/XML/1998/namespace}space", "preserve")
    for node in texts[1:]:
        node.text = ""


def make_paragraph(template, text: str):
    paragraph = clone(template)
    for drawing in paragraph.findall(".//w:drawing", NS):
        parent = next((node for node in paragraph.iter() if drawing in list(node)), None)
        if parent is not None:
            parent.remove(drawing)
    replace_paragraph_text(paragraph, text)
    return paragraph


def clear_cell(cell):
    for child in list(cell):
        if child.tag == qn("w:p"):
            cell.remove(child)


def set_cell(cell, lines: list[str] | str) -> None:
    if isinstance(lines, str):
        lines = [lines]
    template_paragraph = next((child for child in cell if child.tag == qn("w:p")), None)
    if template_paragraph is None:
        template_paragraph = ET.Element(qn("w:p"))

    clear_cell(cell)
    for line in lines:
        cell.append(make_paragraph(template_paragraph, line))


def table_rows(table):
    return table.findall("w:tr", NS)


def row_cells(row):
    return row.findall("w:tc", NS)


def set_table_row(row, values: list[list[str] | str]) -> None:
    cells = row_cells(row)
    for index, value in enumerate(values):
        if index < len(cells):
            set_cell(cells[index], value)


def ensure_table_rows(table, total_rows: int, data_template_index: int = -1) -> None:
    rows = table_rows(table)
    template = rows[data_template_index]
    while len(rows) < total_rows:
        table.append(clone(template))
        rows = table_rows(table)
    while len(rows) > total_rows:
        table.remove(rows[-1])
        rows = table_rows(table)


def set_simple_table(table, left: list[str] | str, right: list[str] | str) -> None:
    row = table_rows(table)[0]
    set_table_row(row, [left, right])


def get_tables(body):
    return [child for child in list(body) if child.tag == qn("w:tbl")]


def body_child_text(child) -> str:
    if child.tag != qn("w:p"):
        return ""
    return paragraph_text(child)


def page_break_paragraph(template):
    paragraph = make_paragraph(template, "")
    run = ET.SubElement(paragraph, qn("w:r"))
    br = ET.SubElement(run, qn("w:br"))
    br.set(qn("w:type"), "page")
    return paragraph


